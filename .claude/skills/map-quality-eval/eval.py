#!/usr/bin/env python3
"""map-quality-eval — SLAM 점유 격자맵 정량 평가 + eval.md 자동 생성.

Usage:
    python3 eval.py <map_name>

전제: docs/evidence/maps/<map_name>/<map_name>.{png,yaml} 존재.
"""
from __future__ import annotations

import datetime as dt
import os
import sys
from pathlib import Path

try:
    from PIL import Image
    import yaml
    import numpy as np
except ImportError as e:
    sys.exit(f"missing dep: {e} — pip3 install Pillow PyYAML numpy")


REPO_ROOT = Path(__file__).resolve().parents[3]
MAPS_DIR = REPO_ROOT / "docs" / "evidence" / "maps"


def classify(occupied_pct: float, unknown_pct: float) -> tuple[str, str]:
    """픽셀 비율로 매핑 패턴 분류 + 다음 액션 추천.

    중요: occupied 낮음 + unknown 낮음은 다음 둘 다 가능:
      (A) 평면 차원 — 회전만 했고 가벽이 LiDAR 반경 3.5m 밖
      (B) 수직 차원 — 가벽 높이가 LDS-03 스캔 평면(약 192mm 지상고)보다 낮음
    PNG·픽셀 통계만으로는 둘 구분 불가 → 사람이 (1) 줄자로 가벽 높이 측정,
    (2) 회전+이동 매핑을 한 번 더 시도해서 비교해야 한다.
    근거: 2026-05-29 회의록 page 3932161에서 (B) 사례 확인 (arena_v1).
    """
    if occupied_pct < 1:
        return ("🟥 매핑 자체 실패", "bringup `/scan` hz 재확인 후 처음부터 재시작")
    if occupied_pct < 3 and unknown_pct < 1:
        return ("⚠️ 가벽 detect 부족 — 평면(거리) 또는 수직(높이) 차원 의심",
                "(1) 줄자로 가벽 상단 높이 측정 → LDS-03 스캔 평면 약 192mm와 비교, "
                "(2) 가벽이 ≥200mm면 회전+이동 매핑 시도, (3) <200mm면 물리 보강 또는 "
                "vision/마커 fallback. 단순 하이브리드 매핑만으로는 (B) 수직 문제는 해결 안 됨.")
    if occupied_pct >= 5 and unknown_pct < 1:
        return ("✅ 회전+이동 OK, 외곽 다 닿음", "발표용 적합, W2 Nav2 진입 가능")
    if occupied_pct >= 5 and unknown_pct >= 10:
        return ("✅ 건강한 매핑", "다음 단계 진입")
    return ("🟡 부분 매핑", "Visual verdict 직접 확인 후 결정")


def use_case_table(occupied_pct: float, files_ok: bool) -> list[tuple[str, str]]:
    """Use case별 go/no-go."""
    return [
        ("데이터 저장 검증",
         "✅ Perfect (3곳 모두)" if files_ok else "🟥 누락"),
        ("W2 SCRUM-10 Nav2 베이스라인",
         "✅" if occupied_pct >= 3 else "🟡 부분 가능 (Nav2가 unknown 너머 진입 위험)"),
        ("발표 시연 (S1)",
         "✅" if occupied_pct >= 5 else "🟥 임팩트 약함"),
        ("Unity 디지털 트윈 텍스처", "✅ 가능 (PNG 정상)"),
    ]


def render_eval_md(map_name: str, meta: dict, stats: dict, verdict: str,
                   recommendation: str, cases: list) -> str:
    """eval.md 본문 생성 — arena_v1과 동일 형식."""
    now = dt.datetime.now().strftime("%Y-%m-%d %H:%M KST")
    real_w = meta["w_px"] * meta["resolution"]
    real_h = meta["h_px"] * meta["resolution"]
    scale_x = real_w / 10
    scale_z = real_h / 10
    ox, oy = meta["origin"][0], meta["origin"][1]

    cases_md = "\n".join(f"| {n} | {v} |" for n, v in cases)
    return f"""# Map Eval — {map_name}

- Date: {now}
- SLAM source: cartographer (multicast) → `tb3-slam-save {map_name}` → `tb3-fetch-map {map_name}`
- Auto-generated by `.claude/skills/map-quality-eval/eval.py`

## Map Output

| 항목 | 값 |
|---|---|
| Image | {meta['w_px']} × {meta['h_px']} px |
| Resolution | {meta['resolution']} m/px |
| Real size | **{real_w:.2f} m × {real_h:.2f} m** |
| Origin (ROS map frame) | [{ox}, {oy}, 0] |
| Unity Plane scale | ({scale_x:.4f}, 1, {scale_z:.4f}) |

## Pixel Statistics

| Category | Pixels | % |
|---|---|---|
| Occupied (검정) | {stats['occupied']:,} | {stats['occupied_pct']:.1f}% |
| Free (흰색) | {stats['free']:,} | {stats['free_pct']:.1f}% |
| Unknown (회색) | {stats['unknown']:,} | {stats['unknown_pct']:.1f}% |

## Verdict

**{verdict}**

→ {recommendation}

## Use Case Assessment

| Use case | Feasible? |
|---|---|
{cases_md}

## Visual Verdict (사람이 PNG 보고 채우기)

- [ ] 외곽이 둥글게 끊김 (LDS-03 반경 한계 노출)
- [ ] 가벽 연결선 끊김
- [ ] 중앙 구조물 깨끗
- [ ] 가벽 두께 일정
- [ ] 격자 무늬 사선/회전 (드리프트)

## Reproduction Commands

```bash
. ~/.tb3rc && . ~/jason/URHYNIX/scripts/tb3.sh
tb3-up
tb3-slam
# (별도 터미널) tb3-teleop
tb3-slam-save {map_name}
tb3-fetch-map {map_name}
tb3-map-to-unity {map_name}
python3 .claude/skills/map-quality-eval/eval.py {map_name}
```

## Related

- DECISION-LOG: 2026-05-29 회전만 매핑의 한계 인식 + 하이브리드 표준화
- 비교 maps: `docs/evidence/maps/` 폴더 다른 매핑들
"""


def main() -> int:
    if len(sys.argv) != 2:
        sys.exit("Usage: python3 eval.py <map_name>")
    name = sys.argv[1]
    out_dir = MAPS_DIR / name
    png = out_dir / f"{name}.png"
    yml = out_dir / f"{name}.yaml"

    if not png.exists():
        sys.exit(f"❌ missing {png} — run `tb3-fetch-map {name}` first")
    if not yml.exists():
        sys.exit(f"❌ missing {yml}")

    # 1) 메타데이터
    meta_y = yaml.safe_load(yml.read_text())
    img = Image.open(png).convert("L")
    arr = np.array(img)
    meta = {
        "w_px": arr.shape[1],
        "h_px": arr.shape[0],
        "resolution": float(meta_y.get("resolution", 0.05)),
        "origin": meta_y.get("origin", [0, 0, 0]),
    }

    # 2) 픽셀 통계
    total = arr.size
    occupied = int((arr < 128).sum())
    free = int((arr > 200).sum())
    unknown = total - occupied - free
    stats = {
        "occupied": occupied, "free": free, "unknown": unknown,
        "occupied_pct": 100 * occupied / total,
        "free_pct":     100 * free / total,
        "unknown_pct":  100 * unknown / total,
    }

    # 3) 분류 + 추천
    verdict, recommendation = classify(stats["occupied_pct"], stats["unknown_pct"])

    # 4) Use case verdict
    robot_path = Path.home() / "maps" / f"{name}.pgm"
    unity_path = REPO_ROOT / "unity-smoke" / "Assets" / "Maps" / f"{name}.png"
    files_ok = (out_dir / f"{name}.pgm").exists() and unity_path.exists()
    cases = use_case_table(stats["occupied_pct"], files_ok)

    # 5) 콘솔 출력
    real_w = meta["w_px"] * meta["resolution"]
    real_h = meta["h_px"] * meta["resolution"]
    print(f"\n📊 Map Eval — {name}")
    print(f"  크기: {meta['w_px']}×{meta['h_px']} px = {real_w:.2f}m × {real_h:.2f}m")
    print(f"  resolution: {meta['resolution']} m/px")
    print(f"  origin: {meta['origin']}")
    print(f"\n  Occupied (가벽): {stats['occupied']:,} px ({stats['occupied_pct']:.1f}%)")
    print(f"  Free    (자유):  {stats['free']:,} px ({stats['free_pct']:.1f}%)")
    print(f"  Unknown (미관측): {stats['unknown']:,} px ({stats['unknown_pct']:.1f}%)")
    print(f"\n  Verdict: {verdict}")
    print(f"  → {recommendation}")
    print(f"\n  Use cases:")
    for n, v in cases:
        print(f"    {v:<55} {n}")

    # 6) eval.md 자동 생성
    eval_md = out_dir / "eval.md"
    if eval_md.exists():
        print(f"\n  ⚠️ {eval_md.name} 이미 존재 — 백업 후 갱신")
        backup = out_dir / f"eval_backup_{dt.datetime.now():%Y%m%d_%H%M%S}.md"
        eval_md.rename(backup)
        print(f"     → backed up to {backup.name}")
    body = render_eval_md(name, meta, stats, verdict, recommendation, cases)
    eval_md.write_text(body)
    print(f"\n  ✅ {eval_md} 생성")
    return 0


if __name__ == "__main__":
    sys.exit(main())
